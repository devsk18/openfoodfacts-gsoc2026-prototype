# search fucntions

# helper
def sanitize(text):
    return text.replace("'", "''")

# fuzzy search
def search_fuzzy(con, brand, product, threshold=0.75, limit=10):
    query = f"{sanitize(brand)} {sanitize(product)}".lower()
    return con.execute(f"""
        SELECT code, brands, name_en,
            ROUND(jaro_winkler_similarity(combined_en, '{query}'), 4) AS score
        FROM processed_products
        WHERE score > {threshold}
        ORDER BY score DESC
        LIMIT {limit}
    """).fetchdf()

# FTS search
def search_fts(con, brand, product, limit=10):
    query = f"{sanitize(brand)} {sanitize(product)}".lower()
    return con.execute(f"""
        SELECT code, brands, name_en,
            ROUND(fts_main_product_fts.match_bm25(code, '{query}'), 4) AS score
        FROM product_fts
        WHERE score IS NOT NULL
        ORDER BY score DESC
        LIMIT {limit}
    """).fetchdf()

