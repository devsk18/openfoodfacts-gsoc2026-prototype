def find_similar_products(
    query_source: str,          # URL or local file path
    top_k: int = 5,
) -> list[dict]:
    """
    End-to-end search:
      URL or path → PIL Image → CLIP embed → Qdrant search → results
    """
    # 1. Load image
    if query_source.startswith("http"):
        img = load_image_from_url(query_source)
    else:
        img = Image.open(query_source).convert("RGB")

    if img is None:
        raise ValueError(f"Could not load image from: {query_source}")

    # 2. Embed
    query_vec = embed_image(img)

    # 3. Search
    hits = search_similar(query_vec, top_k=top_k)

    return hits

import ast
import numpy as np

def extract_name(name_field) -> str:
    if name_field is None:
        return ""
    if isinstance(name_field, (np.ndarray, list)):
        if len(name_field) == 0:
            return ""
        entries = name_field
    else:
        try:
            entries = ast.literal_eval(str(name_field))
        except (ValueError, SyntaxError):
            return str(name_field)
    for lang in ("main", "en"):
        for entry in entries:
            if entry.get("lang") == lang:
                return entry["text"]
    return entries[0].get("text", "")

