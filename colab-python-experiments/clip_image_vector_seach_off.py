def fetch_one(row: dict) -> tuple[str, Image.Image | None]:
    """Download one image. Returns (barcode, PIL_image_or_None)."""
    barcode = row["barcode"]
    url     = row["s3_url"]
    img     = load_image_from_url(url)
    return barcode, img


def fetch_all_images(
    df: pd.DataFrame,
    max_workers: int = 32
) -> tuple[list[str], list[Image.Image], list[str]]:
    """
    Parallel download of all product images.
    Returns (barcodes, images, product_names) — only for successful downloads.
    """
    rows = df.to_dict("records")

    barcodes, images, names = [], [], []
    failed = 0

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        futures = {executor.submit(fetch_one, row): row for row in rows}

        for future in tqdm(
            as_completed(futures),
            total=len(futures),
            desc="Downloading images"
        ):
            barcode, img = future.result()
            row = futures[future]

            if img is None:
                failed += 1
                continue

            barcodes.append(barcode)
            images.append(img)
            names.append(row.get("product_name", ""))

    print(f"Downloaded: {len(images)} | Failed: {failed}")
    return barcodes, images, names


