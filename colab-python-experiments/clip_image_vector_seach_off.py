def load_image_from_url(url: str) -> Image.Image | None:
    """Download image from URL, return PIL Image or None on failure."""
    try:
        resp = requests.get(url, timeout=8)
        resp.raise_for_status()
        return Image.open(BytesIO(resp.content)).convert("RGB")
    except Exception:
        return None

def embed_image(img: Image.Image) -> list[float]:
    """Embed a single PIL Image. Returns a 512-d list."""
    # 1. Preprocess: resize, crop, normalize → tensor of shape [3, 224, 224]
    tensor = preprocess(img).unsqueeze(0).to(device)
    #                               ↑ add batch dim → [1, 3, 224, 224]

    # 2. Encode (no gradient needed for inference)
    with torch.no_grad():
        features = model.encode_image(tensor)

    # 3. L2-normalize — makes cosine similarity = dot product
    features = features / features.norm(dim=-1, keepdim=True)

    # 4. Move to CPU, convert to plain Python list
    return features.squeeze().cpu().tolist()

