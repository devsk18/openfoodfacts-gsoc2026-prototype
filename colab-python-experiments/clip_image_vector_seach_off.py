def build_index(
    barcodes: list[str],
    images:   list[Image.Image],
    names:    list[str],
    embed_batch_size: int = 64,
    upsert_batch_size: int = 256
) -> None:
    total = len(images)

    for i in tqdm(range(0, total, embed_batch_size), desc="Embedding"):
        b_barcodes = barcodes[i : i + embed_batch_size]
        b_images   = images  [i : i + embed_batch_size]
        b_names    = names   [i : i + embed_batch_size]

        b_vectors  = embed_images_batch(b_images, batch_size=embed_batch_size)
        b_payloads = [
            { "product_name": extract_name(name) } for name in b_names
        ]
        upsert_to_qdrant(b_barcodes, b_vectors, b_payloads)

    print(f"\nIndex complete. Total points: {client.get_collection(COLLECTION).points_count}")

