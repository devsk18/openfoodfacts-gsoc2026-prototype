from qdrant_client.models import PointStruct
import uuid

def upsert_to_qdrant(
    barcodes: list[str],
    vectors: list[list[float]],
    payloads: list[dict],
    batch_size: int = 256
) -> None:
    for i in range(0, len(barcodes), batch_size):
        chunk_barcodes = barcodes[i : i + batch_size]
        chunk_vectors  = vectors [i : i + batch_size]
        chunk_payloads = payloads[i : i + batch_size]

        points = [
            PointStruct(
                id=str(uuid.uuid4()),
                vector=vec,
                payload={"barcode": bc, **payload}
            )
            for bc, vec, payload in zip(chunk_barcodes, chunk_vectors, chunk_payloads)
        ]

        client.upsert(collection_name=COLLECTION, points=points)

def search_similar(
    query_vector: list[float],
    top_k: int = 5
) -> list[dict]:
    results = client.query_points(
        collection_name=COLLECTION,
        query=query_vector,
        limit=top_k,
        with_payload=True
    )

    return [
        {
            "barcode":      hit.payload["barcode"],
            "product_name": hit.payload.get("product_name", ""),
            "score":        round(hit.score, 4)
        }
        for hit in results.points
    ]

